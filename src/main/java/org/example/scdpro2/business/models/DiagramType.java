package org.example.scdpro2.business.models;
/**
 * Enum representing the different types of diagrams that can exist in the system.
 * The {@code DiagramType} enum is used to categorize the types of diagrams
 * supported in the project, including class diagrams, package diagrams,
 * and interface diagrams.
 */
public enum DiagramType {
    CLASS, PACKAGE, PACKAGE_DIAGRAM, INTERFACE
}

